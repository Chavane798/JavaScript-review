let diaSemana = 3;

switch (diaSemana) {
  case 1:
    console.log("Domingo");
    break;

  case 2:
    console.log("segunda-feira");
    break;

  case 3:
    console.log("Terca-feira");
    break;

  case 4:
    console.log("Quarta-feira");
    break;

  case 5:
    console.log("Quinta-feira");
    break;
  case 6:
    console.log("Sesta-feira");
    break;

  case 7:
    console.log("Sabado");
}
