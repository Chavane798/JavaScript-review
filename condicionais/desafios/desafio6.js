// Crie um sistema de opções para um caixa eletrônico: "1 - Sacar", "2 - Depositar", "3 - Saldo". Use switch para processar a escolha.

let caixa = 2;

switch (caixa) {
  case 1:
    console.log("Sacar");
    break;

  case 2:
    console.log("Depositar");
    break;

  case 3:
    console.log("saldo");
}
